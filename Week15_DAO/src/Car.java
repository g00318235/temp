
public class Car {

	long id;
	String regNum;
	String make;
	int engineCC;
	public long getId(){
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getRegNum() {
		return regNum;
	}
	public void setRegNum(String string) {
		this.regNum = string;
	}
	public String getMake() {
		return make;
	}
	public void setMake(String string) {
		this.make = string;
	}
	public int getEngineCC() {
		return engineCC;
	}
	public void setEngineCC(int EngineCC) {
		this.engineCC= EngineCC;
	}
	
}
