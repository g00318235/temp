
public class Bar {

	long id;
	String name;
	int price;
	int Value;
	int alert;
	public long getId(){
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String string) {
		this.name = string;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int Price) {
		this.price= Price;
	}
	public int getValue() {
		return Value;
	}
	public void setValue(int Value) {
		this.Value= Value;
	}
	public int getAlert() {
		return alert;
	}
	public void setAlert(int Alert) {
		this.alert= Alert;
	}
}
